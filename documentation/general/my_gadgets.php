<h2>Gadgets</h2>

<p>Gadgets are applications you can add to your Social Networking environment that provide a whole range of potential networking functionality. They are much like Apps on an IPhone. You can find many gadgets by searching around the Internet, They can be <strong>added by copying the URL to the Gadget's XML file</strong> into the "Add gadget by URL" field on the Add Gadgets screen of ATutor Social.</p>

<p>You can use a search engine to search for variations of the terms "open social gadgets" to find many more. Once a gadget has been added to ATutor, it becomes available for everyone on that ATutor server to add to their own networking environement by following <strong>Show Available Gadgets</strong></p>

<p>Use <strong>Find Gadgets</strong> to search through the Google gadget repository. When you find a gaadget you want to add, click on "Add to your webpage" then "Get the Code." In the code that gets generated, look for the URL of an XML file, then copy that URL into the "Add gadget by URL" field, then click the "Add Gadget" button.</p>

