<h2>Photo Gallery</h2>

<p>A photo gallery is designed with accessibility in mind, allowing members to share course related photos and personal photos.</p>

<h3>Basic Photo Album Features</h3>

<dl>
<dt>Photo Gallery</dt>
<dd>This is where your personal albums can be found.  Both Private Albums and Shared Albums will be listed here.</dd>

<dt>Profile Album</dt>
<dd>This is an album where you keep your profile pictures. Photos collected here can be linked into your ATutpr profile page, or into your ATutor Social profile. </dd>

<dt>Course Albums</dt>
<dd>This is where you keep all the course albums.  Anyone enrolled in the course is allowed to upload photos to a course album; students are allowed to edit and delete the photos they have uploaded.  Instructors have the privilege to edit and delete any photos within their course albums. </dd>

<dt>Shared Albums</dt>
<dd>Here all albums will be listed that have the "Shared" permission set.  All users have permission to browse and comment on photos in shared albums, but are not allowed to upload or edit photos.  </dd>

<dt>Create Album</dt>
<dd>To create an album, specify the album name and its permissions. Location (geographically), and description are optional.  If you are an instructor, you will have an extra option of "Album Type", which allows you to create "My Album" or "Course Album".  Under "Album Permission", "Private" means this album is not accessible by any other users except yourself, "Shared" means this album will be displayed under the "Shared Albums" tab, and will be accessible by ALL users.</dd>
</dl>


