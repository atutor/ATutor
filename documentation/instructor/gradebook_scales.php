<h3>Gradebook Scales</h3>
<p>ATutor includes a number of predefined, commonly used grade scales, used to convert number grades into some other form, such as a letter grade, or a Pass/Fail grade.  In the Grade Scales submenu choose Add Grade Scale to create your own custom scale(s). You may choose to populate the Grade Scale form with an existing scale, then edit the scale to create a new one. Or, you may create a brand new scale. Once created, new scales become available to you in any of the course you own.</p>

