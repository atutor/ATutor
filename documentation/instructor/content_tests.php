<h2>Associating Tests with Content</h2>
	<p>Under the Tests &amp; Surveys tab of the content editor is a list of tests available for the current course. One or more tests can be associated with a content page, allowing students to test their knowledge immediately after reviewing the content. Associating a test with a content page inserts a link at the bottom of the page, and inserts a link in the content navigation menu as a sub-menu item for the associated  page. </p>

	<p>In addition to choosing a test to associate with a content page by selecting the appropriate checkbox(es), enter a Custom Test Message that will appear along with the link to the test.</p>

	<p>Also see <a href="content_packages.php">Import/Export Content</a> for details on importing and exporting tests with content packages.</p>

	

