<h2>Accessibility</h2>
	<p>Clicking the Accessibility icon performs an analysis of the content for accessibility problems. Recommendations are given and you are given the option to implement or reverse corrections.</p>

	<p>After opening the Accessibility checker, review the report, and notice the number of <em>known</em> and <em>potential</em> problems</p>

	<p>Correct the known problems by reviewing the report, then modify the HTML in the window below. Re-run the accessibility checker again when the known problems are corrected to see a <em>Conditional Pass</em>. Select from the choices available in the potential problems listed, then press <code>Make Decisions</code> to update the report. When all known problems are corrected, and decisions have been made on all potential problems, a <em>Full Pass</em> will be displayed, after which you can be sure the content will be accessible to all your students.</p>

<h2>Content Editor Accessiblity</h2>
<p>The Content Editor includes a number of added features to help make it accessible to assistive technology users, The accesskeys listed below allow navigation through the editor by keyboard. In addition to the accessibility checker in the ATutor content editor, the visual editor includes its own accessibility checker, as well as prompts while authoring, that prevent making accessibility mistakes.</p>
<h3>Content Editor AccessKeys</h3>
<ul>
<li>[Alt-n] Content Tab</li>
<li> [Alt-p] Properties Tab</li>
<li> [Alt-g] Glossary Tab</li>
<li>[Alt-r] Preview Tab</li>
<li>[Alt-a] Accessibility Tab</li>
<li> [Alt-s] Save</li>
</ul>

<h3>Visual Editor AccessKeys</h3>
<ul>
<li>[Alt-q] jump to button bar</li>
<li>[Alt-z] jump to content area of editor/li>
<li>[Alt-x] jump to element path (bottom)/li>
</ul>


