<h2>Test Statistics</h2>

<p>To view a test's statistics, choose the test from the Test/Survey Manager and use the <code>Statistics</code> button.  There are two sets of statistics available for tests.  The first is <em>Question Statistics</em> which shows each question of the test and the number/percentage of students who chose each of the answers. <em>Submission Statistics</em> shows each submission's overall mark and marks given for each test question.  The overall test average is also calculated.</p>

<p><em>Submission Statistics</em> shows each submission's overall mark and marks given for each test question.  The overall test average is also calculated. The statistics data can be filtered by test's date of taken, all or passed or failed students. The filtered data can be downloaded as CSV file.</p>

