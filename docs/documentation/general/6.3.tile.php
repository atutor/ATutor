<h2>6.3 TILE Repository Search</h2>

<p>External content packages can be downloaded from the TILE repository by entering a search term and using the <code>Search</code> button.  Use the <em>Preview</em> link next to a search result to open the TILE content browser, or use <em>Download</em> to retrieve the content package from the TILE repository. Once downloaded, the file can be unpacked with a common archiving application (e.g. WinZip, PKZip, Unzip). Unzip the file into an empty directory and browse the package's content. </p>

<p>Visit the <a href="http://www.inclusivelearning.ca/" target="new">TILE web site</a> for more information about using the repository.</p>

