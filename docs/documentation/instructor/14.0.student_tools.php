<h2>14. Student Tools</h2>
	<p>Student Tools is a collection of features that may be useful for various types of learning activities. As an instructor, you can decide  where these tools can be accessed form: <em>Main Navigation</em>, or <em>Course Home</em>.</p>

	<dl>
	<dt>Main Navigation</dt>
	<dd>Main Navigation is the area appearing below the title of the course on the interface and appear as "tabs" (appearance may vary depending on the theme being used). Student tools that are selected to appear here will have their own tab.</dd>

	<dt>Course Home</dt>
	<dd>Course Home is the "front" page seen when a student enters a course. Student tools that are selected to appear in the Home page will be displayed with an icon and a label.</dd>
	</dl>

	<p>If neither <em>Main Navigation</em> or <em>Course Home</em> options are selected, then that Student Tool will not be accessible by students, or will turned off. An instructor might choose to turn on Student Tools as they are needed, for example turning on the Tests & Surveys tool with a test is available, or turning on the Chat when an online meeting is going to take place.</p>

	<p>Selecting the arrows in the <em>Order</em> column will change the ordering of the student tool as it appears in the <em>Main Navigation</em> and on the <em>Course Home</em> page.</p>
