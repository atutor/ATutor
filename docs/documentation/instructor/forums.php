<h2>8. Forums</h2>
	<p>A Forum is an area that allows course members to communicate in a structured manner through messages. The forums can be enabled or disabled and linked from the main navigation, the course home page, or displayed as a menu module.  These preferences can be specified in the <a href="../instructor/14.0.student_tools.php">Student Tools</a> section of the Manage area.</p>

	<p>Course instructors and students with forum privileges can manage and mediate the forums by deleting, locking, and sticking threads and messages.</p>

