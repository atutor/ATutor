<h2>11. Polls</h2>
	<p>Polls are useful for quickly gathering course member opinions. Instructors and students with poll privileges can post a question with up to seven choices for answers. Unlike <a href="../instructor/15.0.tests_surveys.php">Tests and Surveys</a>, Polls are not graded. Because Polls is a <a href="../instructor/14.0.student_tools.php">Student Tool</a>, it can be enabled, disabled, and positioned according to the Student Tools preferences. </p>

