<h2>15.2.1 Creating Test Questions</h2>
	<p>Test questions are created through the <a href="../instructor/15.2.question_database.php">Question Database</a> by selecting a type of question from the dropdown and using the <code>Create</code> button. There are a variety of different types of questions: multiple choice, true or false, open ended, and Likert. Open ended questions can be single words, a sentence, paragraph, or a single page answer. Options will differ depending on the type of question being created. All new questions, when saved, will be added to the Question Database where they can then be used in Tests or Surveys.</p>

