<h2>Previewing Tests</h2>

<p>To preview the questions of a test or survey, choose the test from the Tests/Survey Manager and use the <code>Preview</code> button. The screen displayed shows what the student will see when he/she takes a test.  Though the exception is previewing <strong>randomized tests</strong>, which displays all of the questions assigned to that test rather than showing a random number of them (what the student will see). </p>

