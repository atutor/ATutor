<h2>12. Properties</h2>
	<p>In the Properties Manager you can adjust and edit several visual, behavioural, and technical details of a course. Options include the course title and description, the default language used, exporting behaviour, and the course access level. Under the Properties Manager, you can also <a href="./12.1.delete_course.php">delete the course</a>. Additional properties are managed by the ATutor administrator, including upload file size limitations, and space limitations for a course. Contact your ATutor administrator if you need these properties changed.</p>

